package day01_variables;

public class Variables2 {
	public static void main(String[] args) {
		byte heightOfTree = 87;
		byte heightOfBuilding = 80;
		System.out.println(heightOfTree - heightOfBuilding);
		
		/*
		 * Declare two short variables
		 * Assign them values
		 * Print their multiplication on the console
		 */
		
		short num1 = 30000;
		short num2 = -12987;
		System.out.println(num1*num2);
		
		/*
		 * Declare two long variables
		 * Assign them numbers
		 * Print their sum on the console
		 */
		
		long l1=1234567, l2=890123456;
		System.out.println(l1+l2);
		
		float fl1 = 1.2f;
		float fl2 = 0.123f;
		System.out.println(fl1+fl2);
		
		/*
		 * Declare two double variables
		 * Assign them values
		 * Print their difference on the console
		 */
		
		double dbl1 = 1.234, dbl2 = 2.34567;
		System.out.println(dbl1+dbl2);
		
		int num = Integer.MIN_VALUE;
		System.out.println(num);
		
		/*
		 * Find the max value of integers
		 */
		
		int numm = Integer.MAX_VALUE;
		System.out.println(numm);
		
		/*
		 * Find the minimum and maximum values of byte data type
		 */
		
		byte num3 = Byte.MIN_VALUE, num4 = Byte.MAX_VALUE;
		System.out.println("Min value of byte " + num3);
		System.out.println("Max value of byte " + num4);
		
		/*
		 * Find the minimum and maximum values of short data type
		 */
		
		short sh1 = Short.MIN_VALUE, sh2 = Short.MAX_VALUE;
		System.out.println("Min Value of Short : " + sh1);
		System.out.println("Max value of Short : " + sh2);
		
		/*
		 * Find the minimum and maximum values of float data type
		 */
		
		float fl3 = Float.MIN_VALUE, fl4 = Float.MAX_VALUE;
		System.out.println("Min value of  float : " + fl3 );
		System.out.println("Max value of float : " + fl4);
		
		/*
		 * Find the minimum and maximum values of double data type
		 */
		
		double dbl5 = Double.MIN_VALUE, dbl6 = Double.MAX_VALUE;
		System.out.println("Min value of double data type is " + dbl5);
		System.out.println("Max value of double data type is " + dbl6);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
