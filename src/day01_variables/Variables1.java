package day01_variables;

public class Variables1 {
	public static void main(String[] args) {
		
		boolean isCold = true;
		System.out.println("Is the weather cold? " + isCold);
		
		boolean isOld = false;
		System.out.println("Is John old? " + isOld);
		
		char letter1 = 'A';
		System.out.println("This is a char variable: " + letter1);
		
		/*
		 * Create a boolean and char variable
		 * Assign them some values
		 * Print them on console in the same line 
		 * Put space them
		 */
		
		boolean isCheap = false;
		char letter2 = 'B';
		System.out.print(isCheap + " ");
		System.out.println(letter2);
	}
}
