package day09_stringmethods;

public class ForLoopQuestion00 {

	public static void main(String[] args) {
		// Type a program to print the numbers from 3 to 50 on the console
		for(int i = 3; i<=50; i++) {
			System.out.print(i + ", ");
		}
		 System.out.println();
		// Type a program to print the numbers from 50 to 3 on the console
		for(int i=50; i>=3; i--) {
			System.out.print(i + ", ");
		}
		
	}

}
