package day03_cast_inc_dec_concat;

public class Increment {

	public static void main(String[] args) {
		 int num1 = 5;
		 num1 = num1 + 3; // ===> num1 + = 3;
		 System.out.println(num1);
		 
		 /*
		  * Create an integer variable and increase it 
		  * by addition and mutiplication operation
		  * Print the result on the console
		  */

		 int num2 = 12;
		 num2 = num2 + 13;
		 System.out.println(num2);
		 num2 = num2 * 2; // ===> num2 * = 2;
		 System.out.println(num2);
		 
		 /*
		  * Create an integer variable increase it by using short way
		  * Use addition and multiplication
		  */
		 
		 int num3 = 5;
		 num3 += 4;
		 System.out.println(num3);
		 num3*=2;
		 System.out.println(num3);
		 
		 int num4 = 7;
		 num4++; // num4 = num4 + 1;   num4+=1;
		 System.out.println(num4);
		 
		 
		 
		 
		 
		 
		 
		 
		 
		 
		 
		 
		 
		 
		 
		 
		 
		 
		 
	}

}
