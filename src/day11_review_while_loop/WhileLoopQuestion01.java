package day11_review_while_loop;

public class WhileLoopQuestion01 {

	public static void main(String[] args) {
		/*
		 * Print integers on the console from 3 to 7 (Inclusive) by using while loop
		 */
		
		int num = 3;
		
		while(num<8) {
			System.out.print(num + " ");
			num++;  // Do not forget the code to finish the while loop
		}

	}

}
